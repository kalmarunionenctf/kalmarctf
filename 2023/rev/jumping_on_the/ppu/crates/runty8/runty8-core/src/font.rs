const fn copy<const N: usize>(mut arr: [Character; N], arrays: &[&[Character]]) -> [Character; N] {
    let mut arr_idx = 0;
    let mut i = 0;
    let mut start = 0;
    while arr_idx < arrays.len() {
        let arr2 = arrays[arr_idx];
        while i < arr2.len() {
            arr[i + start] = arr2[i];

            i += 1;
        }
        start += i;
        i = 0;
        arr_idx += 1;
    }

    arr
}
